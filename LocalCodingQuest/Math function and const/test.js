console.log(Math.sqrt(100)); // 10
console.log(Math.pow(10, 2)); // 100

console.log(Math.floor(12.99)) // 12 - округление вниз до целого числа
console.log(Math.ceil(12.000001)) // 13 - округление вверх до целого числа
console.log(Math.round(12.4000000)) // 12 - округление в зависимости от дробного числа

console.log(Math.abs(-17)); // 17 - возвращает абсолютное значение числа

console.log(Math.random() * 100); // Случайное число от 1 до 100

console.log(Math.max(...[1, 2, 3, 4, 5])) // Поиск минимального значения в массиве и возврат его - оператор spread...

